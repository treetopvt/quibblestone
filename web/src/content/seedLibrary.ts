//  seedLibrary.ts - the hand-written Slice 1 template library
//  (template-model/02-seed-library).
//
//  This is QuibbleStone's tiny, hand-curated content library (README section
//  4 / 7 / 8 - "a tiny hand-written library" is part of the thin vertical
//  slice). Every entry is a plain `Template` object literal built with the
//  `text()` / `blank()` constructors exported from `../engine/template`
//  (template-model/01). This file is PURE DATA - no AI generation, no
//  authoring UI, no back-office content factory (those are explicitly out of
//  scope here; see docs/features/template-model/02-authoring-and-seed-library.md). Adding a
//  new template is a single, documented step: append one object literal to
//  `seedLibrary` below. See ./README.md for the full authoring guide and a
//  copy-paste starter template.
//
//  Story length: most templates here carry 9-10 blanks across several
//  sentences. That is deliberate for multiplayer: blanks are dealt
//  round-robin across the roster (../engine/distribute.ts), so a story only
//  gives every player a turn when it has at least as many blanks as there
//  are players - and it only gives everyone SEVERAL turns when it has 2-3x
//  that.
//
//  QUICK vs FULL (story-selection/01): the library now ALSO carries a handful
//  of deliberately short "quick" stories (4-6 blanks - see the quick block at
//  the end of the array). Length is DERIVED from blank count, not authored: the
//  selection pipeline classifies a template as 'quick' (<= 6 blanks) or 'full'
//  (>= 7) via ../content/length.ts (QUICK_MAX_BLANKS). Quick stories are for a
//  solo player or a tiny group that wants a fast round; full stories are the
//  ones that give a bigger room several turns each. Add a NEW quick story only
//  with 4-6 blanks; add a NEW full story at 8+ blanks - do not drift into the
//  gap between them without a reason (see ./README.md's "quick vs full" note).
//
//  Content tiers & child safety (README section 6): this library ships in TWO
//  tiers, distinguished ONLY by `tags.familySafe`:
//    - FAMILY-SAFE (tags.familySafe = true / ageRating = 'all-ages'): the default
//      set, hand-vetted to the SAME bar a player's submitted word must clear at
//      play time. Humor comes from absurdity (talking llamas, carsick aliens, a
//      sock-eating dragon), never from innuendo or edge.
//    - NON-FAMILY-SAFE (tags.familySafe = false / ageRating = 'teen-plus'): the
//      grown-up set at the END of this array, offered ONLY when the host turns
//      the family-safe toggle OFF (see ../content/familySafe.ts selectTemplates
//      and the server FamilySafeContentSelector). Register is cheeky/adult
//      (dating, nightlife, office, parties): innuendo and adult THEMES, never
//      explicit content, slurs, or hate.
//  The family-safe toggle is the ONLY gate over these tiers. The runtime
//  profanity/safety filter (child-safety/01) is a separate, complementary guard
//  on player-submitted free text that ALWAYS runs regardless of the toggle -
//  this file does not reimplement it, and turning family-safe off never relaxes
//  it. A few family-safe templates (the secret recipe, the fame guide, the new
//  neighbor) are deliberately OPEN-ENDED, but their authored text still clears
//  the all-ages bar; open-ended never means edgy.
//
//  ======================== KEEP THE SERVER MIRROR IN SYNC ====================
